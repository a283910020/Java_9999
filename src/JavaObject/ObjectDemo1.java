package JavaObject;

public class ObjectDemo1 {

    public String name;
    public int level;
    public String job;


    public void doSkill() {
        System.out.println("ssss");
    }

    public String getName() {
        return name;
    }

    public int getLevel() {
        return level;
    }

    public void setLevel(int level) {
        this.level = level;
    }

    public String getJob() {
        return job;
    }

    public void setJob(String job) {
        this.job = job;
    }

    public void setName(String name) {
        this.name = name;
    }
}
